USER_PROMPT_PLAN_UPDATE = _load_prompt("USER_PROMPT_PLAN_UPDATE")
PLAN_UPDATE_FINAL_PLAN_REDACTION = _load_prompt("PLAN_UPDATE_FINAL_PLAN_REDACTION")
