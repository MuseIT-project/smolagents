from pathlib import Path
import os

PROMPTS_DIR = os.getenv('CUSTOM_PROMPTS_DIR', Path(__file__).parent / "PROMPTS")

def _load_prompt(name: str) -> str:
    with open(f"{PROMPTS_DIR}/{name}.config", "r", encoding="utf-8") as f:
        return f.read()

# Load all prompts
SINGLE_STEP_CODE_SYSTEM_PROMPT = _load_prompt("SINGLE_STEP_CODE_SYSTEM_PROMPT")
TOOL_CALLING_SYSTEM_PROMPT = _load_prompt("TOOL_CALLING_SYSTEM_PROMPT")
